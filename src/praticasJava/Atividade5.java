package praticasJava;

public class Atividade5 {

	public static void main(String[] args) {

		int num1 = 1, num2 = 2;
		
		if (num1 == num2)
			System.out.println("Num1: " + num1 + " is equal to num2  " + num2);
		else if (num1 > num2)
			System.out.println("Num1: " + num1 + " is greater to num2 " + num2);
		else
			System.out.println("Num1: " + num1 + " is less to num2 " + num2);
	}

}
